# ============================================================
# STEP 1: DETECT CONNECTED DRIVES (SIMPLIFIED FOR DEMO)
# ============================================================
def list_drives():
    """Lists all drives on the system. This is a simplified version."""



    
    drives = []
    # For Windows
    if os.name == 'nt':
        import string
        from ctypes import windll
        bitmask = windll.kernel32.GetLogicalDrives()
        for letter in string.ascii_uppercase:
            if bitmask & 1:
                drives.append(f"{letter}:\\")
            bitmask >>= 1
    # For Linux/Mac - This is a common approach
    else:
        # Look in the /sys/block directory to find drives
        if os.path.exists('/sys/block'):
            for device in os.listdir('/sys/block'):
                if device.startswith(('sd', 'hd', 'nvme')): # Common disk prefixes
                    drives.append(f"/dev/{device}")
        else:
            # Fallback: Use a common mount point list
            drives = ["/dev/sda", "/dev/sdb"] # Likely guesses for demo
    return drives

# ============================================================
# STEP 2: PERFORM A SIMPLE 1-PASS WIPE (WRITING ZEROS)
# ============================================================
def wipe_drive(drive_path, passes=1):
    """Wipes a test file on the drive by writing zeros to it."""
    # Create a test file on the drive instead of wiping the whole drive
    test_file = os.path.join(drive_path, "secure_erase_test.bin")
    total_size = 1024 * 1024 * 10  # 10 MB for DEMO
    block_size = 1024 * 64  # 64 KB
    zero_block = b'\0' * block_size

    try:
        print(f"[!] DEMO: This will create and wipe a test file on {drive_path}!")
        print(f"[!] File: {test_file}")
        confirm = input("[?] Type 'YES, WIPE' to confirm: ")
        if confirm != 'YES, WIPE':
            print("[!] Wipe cancelled.")
            return False

        print(f"[+] Creating and wiping test file on {drive_path}...")
        
        # Create and wipe the test file
        with open(test_file, 'wb') as file:
            written = 0
            while written < total_size:
                file.write(zero_block)
                written += len(zero_block)
                # Show a simple progress bar
                progress = (written / total_size) * 100
                sys.stdout.write(f"\r[+] Progress: [{('#' * int(progress//5)):<20}] {progress:.1f}%")
                sys.stdout.flush()
        
        # Delete the test file
        os.remove(test_file)
        print("\n[+] Wipe completed successfully. Test file created and destroyed.")
        return True

    except PermissionError:
        print(f"[!] ERROR: Permission denied for {drive_path}.")
        return False
    except Exception as e:
        print(f"[!] ERROR during wipe: {e}")
        return False

