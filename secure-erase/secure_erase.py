# STEP 3: GENERATE A SIMPLE "CERTIFICATE"
# ============================================================
def generate_certificate(drive_path, algorithm="Quick Wipe (1-Pass Zero)"):
    """Generates a simple text certificate for the wipe operation."""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    # Create a unique hash for this certificate based on time and drive
    cert_data = f"{drive_path}-{timestamp}-{algorithm}".encode()
    cert_hash = hashlib.sha256(cert_data).hexdigest()[:16]  # First 16 chars of hash

    certificate = f"""
=== SECURE-ERASE WIPE CERTIFICATE ===
Unique Certificate ID: {cert_hash}
Drive Wiped: {drive_path}
Wipe Algorithm: {algorithm}
Wipe Verification: Success - No recoverable data found (Simulated)
Date & Time: {timestamp}
Generated By: Secure-Erase MVP
=== END CERTIFICATE ===
"""
    # Save to a file
    filename = f"erase_certificate_{cert_hash}.txt"
    with open(filename, 'w') as f:
        f.write(certificate)
    print(f"[+] Certificate generated: {filename}")
    return certificate

